package com.kaurihealth.datalib.response_bean;

import com.litesuits.orm.db.annotation.PrimaryKey;
import com.litesuits.orm.db.enums.AssignType;

import java.io.Serializable;

/**
 * Created by Nick on 24/04/2016.
 *医生学历的参数  {@link RelatedDoctorBean}
 */
public class DoctorEducationDisplayBean implements Serializable{
    // 指定自增，每个对象需要有一个主键
    @PrimaryKey(AssignType.AUTO_INCREMENT)
    private int id;
    /// <summary>
    /// 医生学历ID
    /// </summary>
    public int doctorEducationId;

    /// <summary>
    /// 医生ID
    /// </summary>
    public int doctorId;

    /// <summary>
    /// 教育程度
    /// </summary>
    public String educationLevel;

    /// <summary>
    /// 教育头衔
    /// </summary>
    public String educationTitle;

    /// <summary>
    /// 教育史
    /// </summary>
    public String educationHistory;

    /// <summary>
    /// 教育责任
    /// </summary>
    public String educationResponsibility;

    /// <summary>
    /// 研究方向
    /// </summary>
    public String researchDirection;

    /// <summary>
    /// 论文
    /// </summary>
    public String thesis;

    /// <summary>
    /// 奖项
    /// </summary>
    public String reward;

    public int getDoctorEducationId() {
        return doctorEducationId;
    }

    public void setDoctorEducationId(int doctorEducationId) {
        this.doctorEducationId = doctorEducationId;
    }

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public String getEducationLevel() {
        return educationLevel;
    }

    public void setEducationLevel(String educationLevel) {
        this.educationLevel = educationLevel;
    }

    public String getEducationTitle() {
        return educationTitle;
    }

    public void setEducationTitle(String educationTitle) {
        this.educationTitle = educationTitle;
    }

    public String getEducationHistory() {
        return educationHistory;
    }

    public void setEducationHistory(String educationHistory) {
        this.educationHistory = educationHistory;
    }

    public String getEducationResponsibility() {
        return educationResponsibility;
    }

    public void setEducationResponsibility(String educationResponsibility) {
        this.educationResponsibility = educationResponsibility;
    }

    public String getResearchDirection() {
        return researchDirection;
    }

    public void setResearchDirection(String researchDirection) {
        this.researchDirection = researchDirection;
    }

    public String getThesis() {
        return thesis;
    }

    public void setThesis(String thesis) {
        this.thesis = thesis;
    }

    public String getReward() {
        return reward;
    }

    public void setReward(String reward) {
        this.reward = reward;
    }
}
