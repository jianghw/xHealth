public class NotifyConfigDisplayBean {


    /**
     * notifyConfigId : 1
     * userId : 2
     * isNotify : true
     */

    private int notifyConfigId;
    private int userId;
    private boolean isNotify;

    public int getNotifyConfigId() {
        return notifyConfigId;
    }

    public void setNotifyConfigId(int notifyConfigId) {
        this.notifyConfigId = notifyConfigId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public boolean isIsNotify() {
        return isNotify;
    }

    public void setIsNotify(boolean isNotify) {
        this.isNotify = isNotify;
    }
}
